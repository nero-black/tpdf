package com.tpdf.shoot.vo;

import java.util.Date;

public class VideoVo {
	private int video_idx;
	private String title;
	private String thumbnail;
	private String video_source;
	private String file;
	private String content;
	private Date wdate;
	private String wdate_d;
	private int hit;
	private String search_value;
	
	
	
	public int getVideo_idx() {
		return video_idx;
	}
	public void setVideo_idx(int video_idx) {
		this.video_idx = video_idx;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getThumbnail() {
		return thumbnail;
	}
	public void setThumbnail(String thumbnail) {
		this.thumbnail = thumbnail;
	}
	public String getVideo_source() {
		return video_source;
	}
	public void setVideo_source(String video_source) {
		this.video_source = video_source;
	}
	public String getFile() {
		return file;
	}
	public void setFile(String file) {
		this.file = file;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public Date getWdate() {
		return wdate;
	}
	public void setWdate(Date wdate) {
		this.wdate = wdate;
	}
	public String getWdate_d() {
		return wdate_d;
	}
	public void setWdate_d(String wdate_d) {
		this.wdate_d = wdate_d;
	}
	public int getHit() {
		return hit;
	}
	public void setHit(int hit) {
		this.hit = hit;
	}
	public String getSearch_value() {
		return search_value;
	}
	public void setSearch_value(String search_value) {
		this.search_value = search_value;
	}
	
	
	
	

}
